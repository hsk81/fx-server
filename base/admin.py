from django.contrib import admin

###############################################################################################
###############################################################################################

class BASE_ADMIN (admin.ModelAdmin):

    def queryset (self, request):

        qs = self.model._default_manager.all (with_deleted = True)
        ordering = self.ordering or ()

        if ordering:
            qs = qs.order_by (*ordering)
            
        return qs

