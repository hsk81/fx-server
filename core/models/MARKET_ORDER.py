from core.models import *
from django.db import models

###############################################################################################
###############################################################################################

class MARKET_ORDER (ORDER):

    class Meta:

        app_label = 'core'
        verbose_name_plural = 'market orders'

    ###########################################################################################
    ###########################################################################################
    
    realized_pl = models.DecimalField (
        max_digits = 15, decimal_places = 6, null = True, blank = True
    )
    
    close = models.ForeignKey ('MARKET_ORDER', null = True, blank = True)
    transaction_link = models.IntegerField (null = True, blank = True)

